/**
 * 漢字を含むテキストをひらがなに変換する
 */
export const convertKanjiToHiragana = (text: string): string => {
  let result = text;
  
  // 辞書に登録されている漢字を順次変換
  for (const [kanji, hiragana] of Object.entries(kanjiToHiragana)) {
    result = result.replace(new RegExp(kanji, 'g'), hiragana);
  }
  
  return result;
}; 